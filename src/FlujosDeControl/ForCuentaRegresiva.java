package FlujosDeControl;

public class ForCuentaRegresiva {
    public static void main(String[] args) {
        //Si i es mayor a 0 se va a repetir
        //Va a imprimir numeros desde el 10
        //A medida que se va repitiendo se decrementa 1 en 1
        //Cada vez que se repita va imprimiendo el valor actual de i
        for (int i = 10; i > 0 ;i--){
            System.out.println("i = " + i);
        }
    }
}

