package FlujosDeControl;

public class ForNumeros {
    public static void main(String[] args) {
        //imprimir números del 0 al 5
        //La variable i empieza desde 0
        //Definimos bucle for con la condición: si i es menor o igual a cero se va a repetir
        //Mientras se repite imprime el valor de i y se incrementa en cada vuelta

        for(int i = 0; i <= 5; i++){
            System.out.println("i = " + i);
        }
    }
}

