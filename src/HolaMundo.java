//Modificador y Nombre de la clase
public class HolaMundo {

    //Método main
    public static void main(String[] args) {
        //Imprime un mensaje
        System.out.print("Hola mundo desde java");
    }
}

/* El método main
 * En java este es el método principal y aquí se ejecuta el código
 * public = Indica que es un modificador público
 * static = indica que es un método estático
 * void = significa vacío o sea que no retorna ninguno valor
 * main = Es el método que recibe los siguientes parámetros
 * String[] args = El método es un arreglo del tipo String
 */

/* Imprimir mensaje por pantalla
 * Hacemos uso de la clase System con el atributo, out.print para imprimir un mensaje en la consola
 * También podemos utilizar System.out.println
 * que hace lo mismo que el print pero salta una línea
 */

/* También podemos generar el código con atajos
 * Para generar el método main: main + tab
 * Para generar el código para imprimir en la consola: sout + tab
 */

/* Las llaves {}
 * Las llaves delimitan el contexto por ejemplo tenemos el nombre de la clase
 * Dentro de las llaves {} creamos nuestro método
 * Y dentro de las llaves {} del método main escribimos nuestro código
 */