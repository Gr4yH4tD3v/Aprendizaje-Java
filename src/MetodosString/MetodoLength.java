package MetodosString;

public class MetodoLength {
    public static void main(String[] args) {

        /* Esto sirve para medir o verificar el largo de una cadena de texto
         * Una cadena es un arreglo de caracteres o secuencia de caracteres */
        String nombre = "Christian"; //Tiene 9 caracteres

        /* Este método cuenta la cantidad de caracteres y devuelve un entero
         * Para hacer uso de métodos usamos el "nombre de la variable un punto y el método" */
        System.out.println("La cantidad de caracteres de la variable es: " + nombre.length() + " caracteres");
    }
}
