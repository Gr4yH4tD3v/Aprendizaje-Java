package MetodosString;

public class MetodoToUpperCase {
    public static void main(String[] args) {

        //Este método sirve para convertir toda una cadena de texto en mayúsculas

        //Definimos un String
        String nombre = "Christian";

        //Método que transforma un String a mayúsculas
        System.out.println("nombre.toUpperCase() = " + nombre.toUpperCase());

    }
}

